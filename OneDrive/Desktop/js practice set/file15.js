// switch statement

let day = 2

switch (day){
    case 0: 
    console.log("sunday");
    break; 
    case 1:
        console.log("monday");
        break;
        case 2:
            console.log("tuesday");
            break;
            case 3:
                console.log("wednesday");
                break;
                default:
                    console.log("invalid");
}